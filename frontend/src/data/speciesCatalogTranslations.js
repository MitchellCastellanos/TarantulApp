export function getSpeciesCatalogOverride(slug, lang) {
  const base = (lang || 'es').split('-')[0]
  if (base === 'es') return null
  const row = SPECIES_CATALOG[slug]
  if (!row) return null
  return row[base] || row.en || null
}
